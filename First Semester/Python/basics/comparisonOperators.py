"""
operation	            operator
equality	                ==
inequality	                !=
less than	                <
greater than	            >
less than or equal to	    <=
greater than or equal to	>=
"""

print("Equality:", 5==5)
print("Inequality:", 5!=5)
print("Less than:", 5<10)
print("Greater than:", 5>10)
print("Less than or equal to:", 5<=5)
print("Greater than or equal to:", 5>=5)
